const matchSymbols = buildString({
  loneCodePoints: [],
  ranges: [
    [0x010940, 0x010959]
  ]
});
testPropertyEscapes(
  /^\p{Script_Extensions=Sidetic}+$/u,
  matchSymbols,
  "\\p{Script_Extensions=Sidetic}"
);
testPropertyEscapes(
  /^\p{Script_Extensions=Sidt}+$/u,
  matchSymbols,
  "\\p{Script_Extensions=Sidt}"
);
testPropertyEscapes(
  /^\p{scx=Sidetic}+$/u,
  matchSymbols,
  "\\p{scx=Sidetic}"
);
